// operations.c

// Function 1: Add two integers
int add(int a, int b) {
    return a + b;
}

// Function 2: Subtract two integers
int sub(int a, int b) {
    return a - b;
}

// Function 3: Multiply two integers
int mul(int a, int b) {
    return a * b;
}

// Function 4: Divide two integers (integer division)
int div(int a, int b) {
    return a / b;
}

// Function 5: Calculate the remainder of division
int mod(int a, int b) {
    return a % b;
}

